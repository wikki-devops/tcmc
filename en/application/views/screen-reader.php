                <p class="fs-16 fw-400 text-black mb-5px">
                    A screen reader uses a Text-To-Speech (TTS) engine to translate on-screen information into speech,
                    which can be heard through earphones or speakers. A TTS may be a software application that comes
                    bundled with the screen reader, or it may be a hardware device that plugs into the computer.
                    Originally, before computers had soundcards, screen readers always used hardware TTS devices, but
                    now that soundcards come as standard on all computers many find that a software TTS is preferable.
                    In addition to speech feedback, screen readers are also capable of providing information in Braille.
                    An external hardware device, known as a refreshable Braille display is needed for this. A
                    refreshable Braille display contains one or more rows of cells. Each cell can be formed into the
                    shape of a Braille character, a series of dots that are similar to domino dots in their layout. As
                    the information on the computer screen changes, so does the Braille characters on the display
                    change, providing refreshable information directly from the computer. Whilst it is possible to use
                    either format independently, Braille output is commonly used in conjunction with speech output.

                </p>
