                    A screen reader is a software application that enables people with severe visual impairments to use
                    a computer. Screen readers work closely with the computer’s Operating System (OS) to provide
                    information about icons, menus, dialogue boxes, files and folders. A screen reader provides access
                    to the entire OS that it works with, including many common applications.
